export function formatAthkar(title, list) {
  return `✨ *${title}* ✨\n━━━━━━━━━━━━━━━━━━\n` +
    list.map((a, i) => `${i + 1}) ${a.text}${a.count > 1 ? ` (${a.count} مرات)` : ''}`).join('\n') +
    '\n━━━━━━━━━━━━━━━━━━\n🤲 تقبل الله طاعتكم.';
}

export function formatPrayerInfo(prayerKey) {
  const p = prayerRakaatInfo[prayerKey];
  if (!p) return '';
  return `📖 الفرض: ${p.fard} ركعات${p.sunnahBefore ? `\n✨ السنة القبلية: ${p.sunnahBefore}` : ''}${p.sunnahAfter ? `\n✨ السنة البعدية: ${p.sunnahAfter}` : ''}`;
}
